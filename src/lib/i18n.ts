export function normalizeLocale(value: string | undefined | null): Locale {
  return value === "en" ? "en" : "zh";
}

export function translate(locale: Locale, source: string, values: TranslationValues = {}) {
  let template = locale === "en" ? english[source] ?? source : source;
  if (locale === "en" && template === source) {
    if (source.startsWith("请选择")) template = `Choose ${translate(locale, source.slice(3))}`;
    else if (source.endsWith("库存不足")) template = `${translate(locale, source.slice(0, -4))} is out of stock`;
    else if (source.endsWith("选择数量超出限制")) template = `Too many ${translate(locale, source.slice(0, -8))} options selected`;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

export function createTranslator(locale: Locale) {
  return (source: string, values?: TranslationValues) => translate(locale, source, values);
}
